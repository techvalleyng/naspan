          <p class="mb-3">Nigeria, due to its large arable land size and conducive climate for production, as well as the large number of existing shea trees, is considered to possess the highest potential for shea production and export in the world. If well developed, many believe that the shea sector offers an immense opportunity for the country’s economic diversification drive. Perhaps, even more importantly, it is a pro-poor economic activity with the potential to raise the productivity and income of those engaged in its value chain. </p>
            <p class="mb-3">NAt the base of its value chain (especially nut collection and initial primary processing), are women who are considered vulnerable and disadvantaged despite their crucial contributions to household livelihoods in Nigeria. Therefore, any project focused on up-scaling shea value chain has the potential of reducing household poverty in producing communities and improving Nigeria’s economy as a whole. In Nigeria the trees grow in an extremely wide area of Niger, Nasarawa, Kebbi, Kwara, Kogi, Kastina, Kaduna, Plateau, Gombe, Bauch, Kano, Jigawa, Yobe, Adamawa, Taraba, Borno Benue and Oyo states, with large concentration in Niger Kebbi and Kwara State.</p>
            
