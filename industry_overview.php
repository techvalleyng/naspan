          <p class="mb-3">Nigeria has the largest population of shea trees among the 21 countries in Africa where the trees grow. With annual production of over 360,000 metric tons. About 45% of the land mass in Nigeria is suitable for growing shea trees. </p>
            <p class="mb-3">Although this is a comparative advantage for Nigeria, the shea industry is grossly underdeveloped and yet to fully realize its potential. There has been no record of shea tree cultivation among farmers, and Nigeria is not among the top processors and exporters of shea products. The reason for this situation is lack of collection due to the absence of large-scale buyers. </p>
            
